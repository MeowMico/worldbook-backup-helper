  function normalizePreference(value) {
    const normalized = String(value || '').toLowerCase();
    if (normalized === 'en') return 'en';
    if (normalized === 'zh' || normalized === 'zh-cn') return 'zh-cn';
    return 'auto';
  }

  function resolveLanguage(preference, editorLanguage) {
    const normalized = normalizePreference(preference);
    if (normalized === 'en') return 'en';
    if (normalized === 'zh-cn') return 'zh';
    return String(editorLanguage || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
  }

  function translate(language, key, values = {}) {
    const source = String(key || '');
    const dictionary = language === 'zh' ? ZH : EN;
    const template = dictionary[source] || source;
    return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => (
      Object.prototype.hasOwnProperty.call(values, name) ? String(values[name]) : match
    ));
  }

  function createDomLocalizer(document) {
    const textNodes = [];
    const attributes = [];
    const walker = document.createTreeWalker(document.body, 4);
    let node = walker.nextNode();
    while (node) {
      const source = node.nodeValue || '';
      if (source.trim()) textNodes.push({ node, source });
      node = walker.nextNode();
    }
    for (const element of document.body.querySelectorAll('*')) {
      for (const name of ['title', 'placeholder', 'aria-label']) {
        if (element.hasAttribute(name)) attributes.push({ element, name, source: element.getAttribute(name) || '' });
      }
    }

    return {
      apply(language) {
        document.documentElement.lang = language === 'zh' ? 'zh-CN' : 'en';
        for (const item of textNodes) {
          const leading = item.source.match(/^\s*/)?.[0] || '';
          const trailing = item.source.match(/\s*$/)?.[0] || '';
          item.node.nodeValue = `${leading}${translate(language, item.source.trim())}${trailing}`;
        }
        for (const item of attributes) {
          item.element.setAttribute(item.name, translate(language, item.source));
        }
      },
    };
  }

  return {
    normalizePreference,
    resolveLanguage,
    translate,
    createDomLocalizer,
  };
